def myfun(a,b):
#Return 5% of the sum of a and b
    return sum((a,b))*0.05
print(myfun(20,80))

def myfun(*args):            #* it means take all arguments and put together in tuple
    print(args)
myfun(10,20,30,40,50)

def myfun(*args):
    for item in args:         #it print each value in new line
        print(item)
myfun(1,2,3,4,5)

def myfunc(**kwargs):
    print(kwargs)
    if 'fruit' in kwargs:
        print('my fruit of choice is {}'.format(kwargs['fruit']))
    else:
        print('i did not find any fruit here')
myfunc(fruit='pomegranate',veggie='lettuce')

def myfunc(*args,**kwargs):
    print(args)
    print(kwargs)
    print('I would like to {} {}'.format(args[0],kwargs['food']))
