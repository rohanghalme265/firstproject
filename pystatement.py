# if, elif and else:-

#syntax for an statement-
'''

if some_condition
        execute some code
elif some_other_condition:
         do something different
else:
        do something else

'''

hungry=True
if hungry:
    print('Feed Me')    #Feed Me
else:
    print('I am not hungry')


